// import Test from '@codewars/test-compat';
function accum(s) {
    // your code
    return s.split('').map((item, idx) => {
        let res = ''
        for (let i = 0; i < idx + 1; i++) {
            res += item.toLowerCase()
        }
        return res[0].toUpperCase() + res.slice(1).toLowerCase()
    })
        .join("-")
}
